<?php

namespace protecno\escuelaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * movimientoConectarse
 */
class movimientoConectarse
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var \DateTime
     */
    private $seleccioneFecha;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set seleccioneFecha
     *
     * @param \DateTime $seleccioneFecha
     * @return movimientoConectarse
     */
    public function setSeleccioneFecha($seleccioneFecha)
    {
        $this->seleccioneFecha = $seleccioneFecha;
    
        return $this;
    }

    /**
     * Get seleccioneFecha
     *
     * @return \DateTime 
     */
    public function getSeleccioneFecha()
    {
        return $this->seleccioneFecha;
    }
}