<?php

namespace protecno\escuelaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * aulas
 */
class aulas
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var integer
     */
    private $noDeAula;

    /**
     * @var integer
     */
    private $capacidad;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set noDeAula
     *
     * @param integer $noDeAula
     * @return aulas
     */
    public function setNoDeAula($noDeAula)
    {
        $this->noDeAula = $noDeAula;
    
        return $this;
    }

    /**
     * Get noDeAula
     *
     * @return integer 
     */
    public function getNoDeAula()
    {
        return $this->noDeAula;
    }

    /**
     * Set capacidad
     *
     * @param integer $capacidad
     * @return aulas
     */
    public function setCapacidad($capacidad)
    {
        $this->capacidad = $capacidad;
    
        return $this;
    }

    /**
     * Get capacidad
     *
     * @return integer 
     */
    public function getCapacidad()
    {
        return $this->capacidad;
    }
}