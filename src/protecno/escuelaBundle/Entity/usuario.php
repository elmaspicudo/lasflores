<?php

namespace protecno\escuelaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * usuario
 */
class usuario
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $nombreDelUsuario;

    /**
     * @var string
     */
    private $nombre;

    /**
     * @var string
     */
    private $apellido;

    /**
     * @var string
     */
    private $contraseña;

    /**
     * @var string
     */
    private $eMail;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set nombreDelUsuario
     *
     * @param string $nombreDelUsuario
     * @return usuario
     */
    public function setNombreDelUsuario($nombreDelUsuario)
    {
        $this->nombreDelUsuario = $nombreDelUsuario;
    
        return $this;
    }

    /**
     * Get nombreDelUsuario
     *
     * @return string 
     */
    public function getNombreDelUsuario()
    {
        return $this->nombreDelUsuario;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     * @return usuario
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;
    
        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set apellido
     *
     * @param string $apellido
     * @return usuario
     */
    public function setApellido($apellido)
    {
        $this->apellido = $apellido;
    
        return $this;
    }

    /**
     * Get apellido
     *
     * @return string 
     */
    public function getApellido()
    {
        return $this->apellido;
    }

    /**
     * Set contraseña
     *
     * @param string $contraseña
     * @return usuario
     */
    public function setContraseña($contraseña)
    {
        $this->contraseña = $contraseña;
    
        return $this;
    }

    /**
     * Get contraseña
     *
     * @return string 
     */
    public function getContraseña()
    {
        return $this->contraseña;
    }

    /**
     * Set eMail
     *
     * @param string $eMail
     * @return usuario
     */
    public function setEMail($eMail)
    {
        $this->eMail = $eMail;
    
        return $this;
    }

    /**
     * Get eMail
     *
     * @return string 
     */
    public function getEMail()
    {
        return $this->eMail;
    }
}