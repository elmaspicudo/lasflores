<?php

namespace protecno\escuelaBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * factura
 */
class factura
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var string
     */
    private $expedidoA;

    /**
     * @var string
     */
    private $nombre;

    /**
     * @var string
     */
    private $direccion;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set expedidoA
     *
     * @param string $expedidoA
     * @return factura
     */
    public function setExpedidoA($expedidoA)
    {
        $this->expedidoA = $expedidoA;
    
        return $this;
    }

    /**
     * Get expedidoA
     *
     * @return string 
     */
    public function getExpedidoA()
    {
        return $this->expedidoA;
    }

    /**
     * Set nombre
     *
     * @param string $nombre
     * @return factura
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;
    
        return $this;
    }

    /**
     * Get nombre
     *
     * @return string 
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set direccion
     *
     * @param string $direccion
     * @return factura
     */
    public function setDireccion($direccion)
    {
        $this->direccion = $direccion;
    
        return $this;
    }

    /**
     * Get direccion
     *
     * @return string 
     */
    public function getDireccion()
    {
        return $this->direccion;
    }
}